/** Generate 30 days of sentiment data points with realistic variance */
function generateSentimentHistory(baseScore: number, symbol: string): SentimentDataPoint[] {
  const points: SentimentDataPoint[] = [];
  let score = baseScore;
  for (let i = 29; i >= 0; i--) {
    const date = new Date();
    date.setDate(date.getDate() - i);
    if (date.getDay() === 0 || date.getDay() === 6) continue; // Skip weekends

    const drift = (Math.random() - 0.48) * 8;
    score = Math.max(-100, Math.min(100, score + drift));
    const dailyScore = Math.round(score);

    points.push({
      date: date.toISOString().split('T')[0],
      overallScore: dailyScore,
      sourceBreakdown: {
        newsScore: Math.round(dailyScore + (Math.random() - 0.5) * 15),
        socialScore: Math.round(dailyScore + (Math.random() - 0.5) * 25),
        analystScore: Math.round(dailyScore + (Math.random() - 0.5) * 10),
        aiScore: Math.round(dailyScore + (Math.random() - 0.5) * 12),
      },
      articleCount: Math.floor(Math.random() * 8) + 2,
      mentionCount: Math.floor(Math.random() * 500) + 50,
    });
  }
  return points;
}

