export const generateStockHistory = (basePrice: number = 2800) => {
  const data: { date: string; open: number; high: number; low: number; close: number; volume: number }[] = [];
  let price = basePrice;
  const today = new Date();
  
  for (let i = 365; i >= 0; i--) {
    const date = new Date(today);
    date.setDate(date.getDate() - i);
    if (date.getDay() === 0 || date.getDay() === 6) continue;
    
    const change = (Math.random() - 0.47) * price * 0.025;
    const open = price;
    const close = Math.max(open + change, open * 0.85);
    const high = Math.max(open, close) * (1 + Math.random() * 0.02);
    const low = Math.min(open, close) * (1 - Math.random() * 0.02);
    price = close;
    
    data.push({
      date: date.toISOString().split('T')[0],
      open: Math.round(open * 100) / 100,
      high: Math.round(high * 100) / 100,
      low: Math.round(low * 100) / 100,
      close: Math.round(close * 100) / 100,
      volume: Math.floor(Math.random() * 20000000) + 5000000,
    });
  }
  return data;
};

export const generateIntradayData = (minInterval: number = 1): { date: string; open: number; high: number; low: number; close: number; volume: number }[] => {
  const data: any[] = [];
  let price = 2800;
  const today = new Date();
  // Indian market session: 9:15 AM to 3:30 PM (375 minutes)
  const sessionStart = 9 * 60 + 15; // 9:15 AM in minutes
  const sessionEnd = 15 * 60 + 30;  // 3:30 PM in minutes
  const totalMinutes = sessionEnd - sessionStart; // 375 minutes
  const bars = Math.floor(totalMinutes / minInterval);
  
  for (let i = 0; i < bars; i++) {
    const minutesFromStart = i * minInterval;
    const hours = Math.floor((sessionStart + minutesFromStart) / 60);
    const mins = (sessionStart + minutesFromStart) % 60;
    
    const date = new Date(today);
    date.setHours(hours, mins, 0, 0);
    
    // More volatile intraday moves
    const change = (Math.random() - 0.5) * price * 0.008;
    const open = price;
    const close = Math.max(open + change, open * 0.95);
    const high = Math.max(open, close) * (1 + Math.random() * 0.015);
    const low = Math.min(open, close) * (1 - Math.random() * 0.015);
    price = close;
    
    data.push({
      date: date.toISOString(),
      open: Math.round(open * 100) / 100,
      high: Math.round(high * 100) / 100,
      low: Math.round(low * 100) / 100,
      close: Math.round(close * 100) / 100,
      volume: Math.floor(Math.random() * 5000000) + 500000,
    });
  }
  return data;
};
